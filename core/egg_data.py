def identify_egg(size_m: float, weight_kg: float, top_n: int = 10):
    """
    根据蛋的尺寸(m)和重量(kg)识别可能的精灵

    返回: [(精灵名, 匹配度, 蛋组), ...]  匹配度: "完全匹配"/"部分匹配(尺寸)"/"部分匹配(重量)"/"不匹配"
    """
    results = []
    for name, info in EGGS.items():
        s_min, s_max = info["size"]
        w_min, w_max = info["weight"]
        size_ok = s_min <= size_m <= s_max
        weight_ok = w_min <= weight_kg <= w_max

        if size_ok and weight_ok:
            match = "完全匹配"
        elif size_ok:
            match = "部分匹配(尺寸)"
        elif weight_ok:
            match = "部分匹配(重量)"
        else:
            continue  # 完全不匹配，跳过

        results.append((name, match, info["egg_group"]))

    # 排序：完全匹配优先，然后按重量差绝对值排序
    def _sort_key(item):
        name, match, grp = item
        info = EGGS[name]
        w_center = (info["weight"][0] + info["weight"][1]) / 2
        s_center = (info["size"][0] + info["size"][1]) / 2
        weight_diff = abs(weight_kg - w_center)
        size_diff = abs(size_m - s_center)
        match_score = {"完全匹配": 0, "部分匹配(尺寸)": 1, "部分匹配(重量)": 2}.get(match, 3)
        return (match_score, weight_diff + size_diff * 10)

    results.sort(key=_sort_key)
    return results[:top_n]


