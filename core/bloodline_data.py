def get_bloodlines_for_pet(attributes):
    """根据精灵属性返回可选血脉列表：["无"] + 属性血脉 + ["首领血脉","奇异","污染"]，去重"""
    result = ["无"]
    seen = {"无"}
    for attr in attributes:
        bl = ATTRIBUTE_BLOODLINE_MAP.get(attr)
        if bl and bl not in seen:
            result.append(bl)
            seen.add(bl)
    # 特殊血脉
    for special in ["首领血脉", "奇异", "污染"]:
        if special not in seen:
            result.append(special)
            seen.add(special)
    return result


